    private void enviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enviarActionPerformed
        if (email.getText().isEmpty() || senha.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "H치 campos em branco.\nPor favor, preencha todos os campos.");
        } else {
            EasyMail_Autenticacao_Gmail autenticar = new EasyMail_Autenticacao_Gmail(email.getText().trim(), senha.getText().trim());
            System.out.println(autenticar.estaAutenticado().getDebug());
            if (!autenticar.estaAutenticado().getProperties().isEmpty()) {
                EasyMail_GUI abrirEmail = new EasyMail_GUI();
                JOptionPane.showMessageDialog(null, "Usu치rio autenticado com sucesso. ");
                abrirEmail.setVisible(true);
                abrirEmail.recebeSessao(autenticar.estaAutenticado(), email.getText().trim());
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Usu치rio ou senha inv치lido.\nPor favor, tente novamente. ");
            }
        }

    }//GEN-LAST:event_enviarActionPerformed

